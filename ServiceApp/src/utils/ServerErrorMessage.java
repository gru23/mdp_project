package utils;

public class ServerErrorMessage {
	private String content;
	
	public ServerErrorMessage() {
		super();
	}

	public ServerErrorMessage(String content) {
		super();
		this.content = content;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
}
